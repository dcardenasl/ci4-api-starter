#!/bin/bash
#
# CRUD Validation Script
# Validates scaffolded CRUDs against known issues
#
# Usage:
#   ./bin/validate-crud.sh ResourceName Domain
#

set -e

# Colors
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m'

RESOURCE=${1:-}
DOMAIN=${2:-}

if [[ -z "$RESOURCE" || -z "$DOMAIN" ]]; then
    echo -e "${RED}❌ Missing arguments${NC}"
    echo "Usage: ./bin/validate-crud.sh ResourceName Domain"
    echo "Example: ./bin/validate-crud.sh Product Catalog"
    exit 1
fi

cd "$(dirname "$0")/.."

# Compute domain in kebab-case: TestDomain → test-domain
DOMAIN_KEBAB=$(echo "$DOMAIN" | sed 's/\([A-Z]\)/-\1/g' | sed 's/^-//' | tr '[:upper:]' '[:lower:]')

# Migration stem: strip trailing 'y' so TestUnify → TestUnif matches TestUnifiesTable
RESOURCE_STEM="${RESOURCE%y}"

echo -e "${BLUE}═══════════════════════════════════════════════════${NC}"
echo -e "${BLUE}CRUD Validation: $RESOURCE ($DOMAIN)${NC}"
echo -e "${BLUE}═══════════════════════════════════════════════════${NC}"
echo ""

PASSED=0
FAILED=0

# Test 1: Migration exists
echo -e "${YELLOW}[1/6] Checking migration exists...${NC}"
MIGRATION=$(find app/Database/Migrations -name "*_Create${RESOURCE_STEM}*Table.php" | head -1)
if [[ -n "$MIGRATION" ]]; then
    echo -e "${GREEN}✓${NC} Found: $MIGRATION"
    ((PASSED++))
else
    echo -e "${RED}✗${NC} Migration not found for $RESOURCE"
    ((FAILED++))
fi

# Test 2: Table name is snake_case (if migration found)
if [[ -n "$MIGRATION" ]]; then
    echo ""
    echo -e "${YELLOW}[2/6] Checking table name format...${NC}"
    TABLE_NAME=$(grep "createTable(" "$MIGRATION" | head -1 | awk -F"'" '{print $2}')

    if [[ "$TABLE_NAME" =~ ^[a-z_]+$ ]]; then
        echo -e "${GREEN}✓${NC} Table name is snake_case: $TABLE_NAME"
        ((PASSED++))
    else
        echo -e "${RED}✗${NC} Table name is NOT snake_case: $TABLE_NAME"
        echo "   Fix: Edit migration, change 'createTable(\"$TABLE_NAME\")' to proper snake_case"
        ((FAILED++))
    fi
else
    echo -e "${YELLOW}[2/6] Skipped (migration not found)${NC}"
fi

# Test 3: Soft-delete field check (warning only — may be intentional)
echo ""
echo -e "${YELLOW}[3/6] Checking for deleted_at field...${NC}"
if [[ -n "$MIGRATION" ]]; then
    if grep -q "deleted_at" "$MIGRATION"; then
        echo -e "${YELLOW}⚠${NC} Migration contains 'deleted_at' field"
        echo "   If this resource uses --soft-delete no, remove the deleted_at field"
        ((PASSED++))
    else
        echo -e "${GREEN}✓${NC} No deleted_at field (hard delete or check intentional)"
        ((PASSED++))
    fi
else
    echo -e "${YELLOW}[3/6] Skipped (migration not found)${NC}"
fi

# Test 4: Controller exists (no HasCrudActions trait — explicit methods generated by ControllerGenerator)
echo ""
echo -e "${YELLOW}[4/6] Checking controller exists...${NC}"
CONTROLLER=$(find app/Controllers/Api/V1 -name "${RESOURCE}Controller.php" | head -1)
if [[ -n "$CONTROLLER" ]]; then
    echo -e "${GREEN}✓${NC} Found: $CONTROLLER"
    ((PASSED++))
else
    echo -e "${RED}✗${NC} Controller not found: ${RESOURCE}Controller.php"
    ((FAILED++))
fi

# Test 5: Model exists
echo ""
echo -e "${YELLOW}[5/6] Checking model exists...${NC}"
MODEL=$(find app/Models -name "${RESOURCE}Model.php" | head -1)
if [[ -n "$MODEL" ]]; then
    echo -e "${GREEN}✓${NC} Found: $(basename "$MODEL")"
    ((PASSED++))
else
    echo -e "${RED}✗${NC} Model not found: ${RESOURCE}Model.php"
    ((FAILED++))
fi

# Test 6: Routes file exists and references the resource
echo ""
echo -e "${YELLOW}[6/6] Checking routes registered...${NC}"
ROUTE_FILE="app/Config/Routes/v1/${DOMAIN_KEBAB}.php"
if [[ -f "$ROUTE_FILE" ]]; then
    if grep -q "${RESOURCE}Controller" "$ROUTE_FILE"; then
        echo -e "${GREEN}✓${NC} Found and wired: $ROUTE_FILE"
        ((PASSED++))
    else
        echo -e "${YELLOW}⚠${NC} Route file exists but ${RESOURCE}Controller not found in it"
        echo "   File: $ROUTE_FILE"
        ((FAILED++))
    fi
else
    echo -e "${RED}✗${NC} Route file not found: $ROUTE_FILE"
    ((FAILED++))
fi

# Summary
echo ""
echo -e "${BLUE}═══════════════════════════════════════════════════${NC}"
echo -e "Results: ${GREEN}$PASSED passed${NC}, ${RED}$FAILED failed${NC}"
echo -e "${BLUE}═══════════════════════════════════════════════════${NC}"

if [[ $FAILED -eq 0 ]]; then
    echo -e "${GREEN}✅ All validations passed!${NC}"
    echo ""
    echo "Next steps:"
    echo "  1. php spark migrate"
    echo "  2. php spark module:check $RESOURCE --domain $DOMAIN"
    echo "  3. vendor/bin/phpunit tests/Feature/Controllers/$DOMAIN/${RESOURCE}ControllerTest.php"
    exit 0
else
    echo -e "${RED}❌ Validation failed. Review errors above and fix.${NC}"
    exit 1
fi
